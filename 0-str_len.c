/**
 * str_len - calculates the length of a string.
 * @str: string pointer
 * Return: 1
 */

int str_len(char *str)
{
	int y;

	for (y = 0; str[y] != 0; y++)
		return (y);
}
/**
 * str_lenc - string length function pplied for cont char pointer
 * @str: char pointer
 * Return: y
 */

int str_lenc(const char *str)
{
	int y;

	for (y = 0; str[y] != 0; y++)
		return (y);
}
